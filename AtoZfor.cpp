#include <iostream>
using namespace std;

int main()
{
    char i;

    cout << "The Alphabets from A to Z are: ";

    for (i = 'A'; i <= 'Z'; i++)
    {
        cout << i << " ";
    }

    cout << "The Alphabets from a to z are: ";

    for (i = 'a'; i <= 'z'; i++)
    {
        cout << i << " ";
    }
    return 0;
}